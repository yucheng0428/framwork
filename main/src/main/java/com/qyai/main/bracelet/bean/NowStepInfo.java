package com.qyai.main.bracelet.bean;

/**
 * 获取步数
 */
public class NowStepInfo {


    /**
     * code : 0
     * dataType : 522
     * ecgLocation : 1
     */

    private int code;
    private int dataType;
    private int ecgLocation;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public int getDataType() {
        return dataType;
    }

    public void setDataType(int dataType) {
        this.dataType = dataType;
    }

    public int getEcgLocation() {
        return ecgLocation;
    }

    public void setEcgLocation(int ecgLocation) {
        this.ecgLocation = ecgLocation;
    }
}
