package com.qyai.main.bracelet.bean;

/**
 * 历史记录信息
 */
public class HistoryOutInfo {


    /**
     * SleepNum : 0 睡眠条数
     * SleepTotalTime : 0 睡眠总条数
     * TempHumidNum : 0 温湿度条数
     * code : 0
     * SportNum : 0 运动条数
     * BloodOxygenNum : 0 血氧条数
     * AmbientLightNum : 0 环境光条数
     * dataType : 525 步数
     * supportOk : 0
     * TempNum : 0 体温条数
     * HeartNum : 0 心率条数
     * BloodNum : 0 血压条数
     */


    private int SleepNum;
    private int SleepTotalTime;
    private int TempHumidNum;
    private int code;
    private int SportNum;
    private int BloodOxygenNum;
    private int AmbientLightNum;
    private int dataType;
    private int supportOk;
    private int TempNum;
    private int HeartNum;
    private int BloodNum;

    public int getSleepNum() {
        return SleepNum;
    }

    public void setSleepNum(int SleepNum) {
        this.SleepNum = SleepNum;
    }

    public int getSleepTotalTime() {
        return SleepTotalTime;
    }

    public void setSleepTotalTime(int SleepTotalTime) {
        this.SleepTotalTime = SleepTotalTime;
    }

    public int getTempHumidNum() {
        return TempHumidNum;
    }

    public void setTempHumidNum(int TempHumidNum) {
        this.TempHumidNum = TempHumidNum;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public int getSportNum() {
        return SportNum;
    }

    public void setSportNum(int SportNum) {
        this.SportNum = SportNum;
    }

    public int getBloodOxygenNum() {
        return BloodOxygenNum;
    }

    public void setBloodOxygenNum(int BloodOxygenNum) {
        this.BloodOxygenNum = BloodOxygenNum;
    }

    public int getAmbientLightNum() {
        return AmbientLightNum;
    }

    public void setAmbientLightNum(int AmbientLightNum) {
        this.AmbientLightNum = AmbientLightNum;
    }

    public int getDataType() {
        return dataType;
    }

    public void setDataType(int dataType) {
        this.dataType = dataType;
    }

    public int getSupportOk() {
        return supportOk;
    }

    public void setSupportOk(int supportOk) {
        this.supportOk = supportOk;
    }

    public int getTempNum() {
        return TempNum;
    }

    public void setTempNum(int TempNum) {
        this.TempNum = TempNum;
    }

    public int getHeartNum() {
        return HeartNum;
    }

    public void setHeartNum(int HeartNum) {
        this.HeartNum = HeartNum;
    }

    public int getBloodNum() {
        return BloodNum;
    }

    public void setBloodNum(int BloodNum) {
        this.BloodNum = BloodNum;
    }
}
